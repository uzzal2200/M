/* ----- LED ব্লিংকিং (LED Blinking) প্রোগ্রাম -----
 * এই প্রোগ্রামটি দুটি LED কে পর্যায়ক্রমে জ্বালিয়ে নিভিয়ে ব্লিংক করায়।
 *
 * হার্ডওয়্যার সংযোগ:
 * - PORTB: আউটপুট পোর্ট হিসেবে কনফিগার করা (TRISB = 0x00)
 * - RB0 (PORTB.F0): প্রথম LED সংযুক্ত
 * - RB1 (PORTB.F1): দ্বিতীয় LED সংযুক্ত
 *
 * কিভাবে কাজ করে:
 * ১. TRISB = 0x00: PORTB এর সব পিন আউটপুট মোড়ে সেট করে
 * ২. PORTB = 0x00: প্রারম্ভিক অবস্থায় সব পিন LOW (LED বন্ধ)
 * ৩. while(1): অসীম লুপ - প্রোগ্রাম চিরকাল চলবে
 *
 * LED নিয়ন্ত্রণ:
 * - PORTB.F0 = 1, F1 = 0: প্রথম LED জ্বালে, দ্বিতীয় নিভায়
 * - delay_ms(500): ৫০০ মিলিসেকেন্ড (০.৫ সেকেন্ড) অপেক্ষা
 * - PORTB.F0 = 0, F1 = 1: প্রথম LED নিভায়, দ্বিতীয় জ্বালে
 * - delay_ms(500): আবার ৫০০ মিলিসেকেন্ড অপেক্ষা
 *
 * ব্লিংক প্যাটার্ন:
 * - LED1 চালু → ০.৫ সেকেন্ড → LED1 বন্ধ + LED2 চালু → ০.৫ সেকেন্ড → পুনরাবৃত্তি
 * - মোট সাইকেল টাইম: ১ সেকেন্ড
 * - বিকল্প ব্লিংক (Alternate Blinking) প্যাটার্ন
 *
 * ব্যবহার: ট্রাফিক লাইট, সতর্কীকরণ সংকেত, ইন্ডিকেটর লাইট, ডেকোরেশন ইত্যাদিতে।
 */