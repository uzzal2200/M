void MSDelay(unsigned char Time)
{
    unsigned char y,z;
    for(y=0;y<Time;y++)
        for(z=0;z<250;z++);
}

void main()
{
    TRISC = 0x00;
    TRISD = 0x00;
    while(1)
    {
        PORTD = 0x80;
        PORTC = 0x00;
        MSDelay(10);

        PORTD = 0x40;
        PORTC = 0xFF;
        MSDelay(10);

        PORTD = 0x20;
        PORTC = 0xFF;
        MSDelay(10);

        PORTD = 0x10;
        PORTC = 0x18;
        MSDelay(10);

        PORTD = 0x08;
        PORTC = 0x18;
        MSDelay(10);

        PORTD = 0x04;
        PORTC = 0xFF;
        MSDelay(10);

        PORTD = 0x02;
        PORTC = 0xFF;
        MSDelay(10);

        PORTD = 0x01;
        PORTC = 0x00;
        MSDelay(10);
    }
}

/* ----- ডট ম্যাট্রিক্স ডিসপ্লে (Dot Matrix Display) প্রোগ্রাম -----
 * এই প্রোগ্রামটি ৮x৮ LED ডট ম্যাট্রিক্স ডিসপ্লে নিয়ন্ত্রণ করে এবং বিভিন্ন প্যাটার্ন প্রদর্শন করে।
 *
 * হার্ডওয়্যার সংযোগ:
 * - PORTC: কলাম (Column) নিয়ন্ত্রণের জন্য (৮টি LED কলাম)
 * - PORTD: রো (Row) নিয়ন্ত্রণের জন্য (৮টি LED রো)
 * - ৮x৮ = ৬৪টি LED একসাথে নিয়ন্ত্রণ করা হয়
 *
 * কিভাবে কাজ করে:
 * ১. MSDelay() ফাংশন: কাস্টম বিলম্ব তৈরি করে (সফটওয়্যার বিলম্ব)
 * ২. PORTD এ বিট প্যাটার্ন পাঠিয়ে সক্রিয় রো নির্বাচন করে
 * ৩. PORTC এ প্যাটার্ন পাঠিয়ে সেই রো এর কোন LED গুলো জ্বালাতে হবে নির্ধারণ করে
 *
 * প্যাটার্ন ব্যাখ্যা:
 * - PORTD = 0x80 (10000000): ৮ম রো সক্রিয়, PORTC = 0x00: সব LED বন্ধ
 * - PORTD = 0x40 (01000000): ৭ম রো সক্রিয়, PORTC = 0xFF: সব LED চালু
 * - PORTD = 0x20 (00100000): ৬ম রো সক্রিয়, PORTC = 0xFF: সব LED চালু
 * - PORTD = 0x18 (00011000): বিশেষ প্যাটার্ন
 *
 * মাল্টিপ্লেক্সিং কৌশল:
 * - একবারে একটি রো সক্রিয় করে দ্রুত সুইচিং
 * - চোখে পূর্ণাঙ্গ ছবি দেখানোর জন্য পার্সিসটেন্স অফ ভিশন ব্যবহার
 * - ১০ms বিলম্বে প্রতিটি রো আপডেট
 *
 * ব্যবহার: LED সাইনবোর্ড, স্কোরবোর্ড, ডিজিটাল ঘড়ি, গেমিং ডিসপ্লে ইত্যাদিতে।
 */
